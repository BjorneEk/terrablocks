	BufferedImage background;
	Game g;

	public StartMenu() {
		int w;
		int h;
		BufferedImage     after = null;
		AffineTransform   at    = new AffineTransform();
		AffineTransformOp scaleOp;
		//this.setSize(1080, 1920);
		initComponents();
		try {
			background = ImageIO.read(new File("src/Assets/startmenu.png"));
			w = background.getWidth();
			h = background.getHeight();
			after = new BufferedImage(w*4, h*4, BufferedImage.TYPE_INT_ARGB);
			at.scale(3.3, 3.7);
			scaleOp = new AffineTransformOp(at, AffineTransformOp.TYPE_BILINEAR);
			after = scaleOp.filter(background, after);
			background = after;
		} catch (IOException e) {
		}
	}

	@Override
	public void paintComponent(Graphics g) {
		super.paintComponent(g);
		g.setColor(Color.GREEN);
		g.fillRect(0, 0, super.getWidth(),super.getHeight());
		g.drawImage(background, 0, 0, null);
		g.setColor(Color.BLACK);
		Font f = new Font("Dialog", Font.BOLD, 100);
		g.setFont(f);
		g.drawString("TerraBlocks!" ,(super.getWidth() / 2)-300, 250);
	}

